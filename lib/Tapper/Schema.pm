package Tapper::Schema;
# ABSTRACT: Tapper - Database schemas for the Tapper test framework

use warnings;
use strict;

1; # End of Tapper::Schema

=head1 DESCRIPTION

This distribution contains the database schemas for 
the L<Tapper> test framework.

L<Tapper> is based on the Test Anything Protocol (TAP),
which is the test I/O format used by all CPAN modules,
driven by M<Test::Harness>.

=head1 SEE ALSO

L<Tapper> - the documentation for the Tapper framework.

L<testanything.org|http://testanything.org> - the home page
for the Test Anything Protocol (TAP).

